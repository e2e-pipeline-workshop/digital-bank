package io.digisic.bank.model.obp;

public class AccountAttributes {

	private String product_code;
	private String account_attribute_id;
	private String name;
	private String type;
	private String value;
	
	/**
	 * @return the product_code
	 */
	public String getProduct_code() {
		return product_code;
	}
	/**
	 * @param product_code the product_code to set
	 */
	public void setProduct_code(String product_code) {
		this.product_code = product_code;
	}
	/**
	 * @return the account_attribute_id
	 */
	public String getAccount_attribute_id() {
		return account_attribute_id;
	}
	/**
	 * @param account_attribute_id the account_attribute_id to set
	 */
	public void setAccount_attribute_id(String account_attribute_id) {
		this.account_attribute_id = account_attribute_id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
